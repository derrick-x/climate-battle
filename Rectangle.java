
public class Rectangle {
	int xPos1;
	int yPos1;
	int xPos2;
	int yPos2;
	public Rectangle(int x1, int y1, int x2, int y2) {
		xPos1 = x1;
		yPos1 = y1;
		xPos2 = x2;
		yPos2 = y2;
	}
	public int getX1() {
		return xPos1;
	}
	public int getY1() {
		return yPos1;
	}
	public int getX2() {
		return xPos2;
	}
	public int getY2() {
		return yPos2;
	}
}
